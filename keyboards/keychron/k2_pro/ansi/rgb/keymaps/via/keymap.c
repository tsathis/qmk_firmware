bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  bool vim_handled = handle_vim_mode(keycode, record, _VIM);
  if (vim_handled)
    return false;

  switch (keycode) {
    case HAPPY: {
      if (!record->event.pressed) {
        SEND_STRING(":) ");
      }
      return false;
    }
    break;
    case SAD: {
      if (!record->event.pressed) {
        SEND_STRING(":( ");
      }
      return false;
    }
    break;
    case TONGUE: {
      if (!record->event.pressed) {
        SEND_STRING(":p ");
      }
      return false;
    }
    break;
  }
  return true;
}
